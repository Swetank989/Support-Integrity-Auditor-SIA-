    "df['Signal_2_Sim'] = (cos_sim_high - cos_sim_low + 1.0) / 2.0\n",
    "df['Signal_2'] = 0.5 * df['Signal_2_Cluster'] + 0.5 * df['Signal_2_Sim']\n",
    "\n",
    "print(\"Generating Signal 3...\")\n",
