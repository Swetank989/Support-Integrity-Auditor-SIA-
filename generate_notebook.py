    "# Define keywords for Signal 1\n",
    "high_urgency_words = [\n",
    "    \"urgent\", \"immediate\", \"asap\", \"broken\", \"crashed\", \"outage\", \"blocked\", \n",
    "    \"prevent\", \"cannot access\", \"security\", \"compromised\", \"down\", \"critical\", \n",
    "    \"emergency\", \"lost data\", \"loss of data\", \"not working\", \"fail\", \"not charging\",\n",
    "    \"not turning on\", \"no power\", \"unusable\", \"freeze\", \"crash\", \"disabled\", \"hacked\"\n",
    "]\n",
    "escalation_phrases = [\n",
    "    \"contacted multiple times\", \"unresolved\", \"frustrated\", \"refund\", \"cancel my subscription\",\n",
    "    \"legal\", \"warn\", \"disappointed\", \"poor customer service\", \"unacceptable\", \"terrible\",\n",
    "    \"waiting for days\", \"stuck\", \"still not\", \"help me\"\n",
    "]\n",
    "\n",
    "def calculate_nlp_score(text):\n",
    "    text = text.lower()\n",
    "    score = 0.0\n",
    "    for w in high_urgency_words:\n",
    "        if w in text:\n",
    "            score += 2.0\n",
    "    for p in escalation_phrases:\n",
    "        if p in text:\n",
    "            score += 1.5\n",
    "    negations = [\"not\", \"no\", \"never\", \"cannot\", \"doesn't\", \"don't\", \"wasn't\"]\n",
    "    tokens = text.split()\n",
    "    for i, tok in enumerate(tokens):\n",
    "        if tok in negations and i + 1 < len(tokens):\n",
    "            next_tok = tokens[i+1]\n",
    "            if next_tok in [\"working\", \"charging\", \"turning\", \"responding\", \"connecting\", \"opening\"]:\n",
    "                score += 1.5\n",
    "    return score\n",
    "\n",
    "print(\"Generating Signal 1...\")\n",
    "df['Signal_1_Raw'] = df['Full Text'].apply(calculate_nlp_score)\n",
    "s1_min, s1_max = df['Signal_1_Raw'].min(), df['Signal_1_Raw'].max()\n",
    "df['Signal_1'] = (df['Signal_1_Raw'] - s1_min) / (s1_max - s1_min + 1e-9)\n",
    "\n",
    "print(\"Generating Signal 2...\")\n",
    "model_st = SentenceTransformer('all-MiniLM-L6-v2')\n",
    "embeddings = model_st.encode(df['Full Text'].tolist(), show_progress_bar=True)\n",
    "\n",
    "kmeans = KMeans(n_clusters=5, random_state=42)\n",
    "df['Cluster'] = kmeans.fit_predict(embeddings)\n",
    "\n",
    "priority_map = {'Low': 0, 'Medium': 1, 'High': 2, 'Critical': 3}\n",
    "df['Priority_Numeric'] = df['Ticket Priority'].map(priority_map)\n",
    "\n",
    "cluster_priority = df.groupby('Cluster')['Priority_Numeric'].mean()\n",
    "cluster_rank = cluster_priority.rank(method='first') - 1\n",
    "cluster_rank_map = (cluster_rank / 4.0).to_dict()\n",
    "df['Signal_2_Cluster'] = df['Cluster'].map(cluster_rank_map)\n",
    "\n",
    "ref_high_emb = model_st.encode(\"emergency critical failure down crash blocked cannot access loss of data broken not working\")\n",
    "ref_low_emb = model_st.encode(\"general inquiry information request how to change settings setup guidelines question\")\n",
    "\n",
    "cos_sim_high = np.dot(embeddings, ref_high_emb) / (np.linalg.norm(embeddings, axis=1) * np.linalg.norm(ref_high_emb) + 1e-9)\n",
