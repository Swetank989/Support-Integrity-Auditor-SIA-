    "\n",
    "df['Pred_Resolution_Duration'] = reg.predict(embeddings)\n",
    "s3_min, s3_max = df['Pred_Resolution_Duration'].min(), df['Pred_Resolution_Duration'].max()\n",
    "df['Signal_3'] = (df['Pred_Resolution_Duration'] - s3_min) / (s3_max - s3_min + 1e-9)"
