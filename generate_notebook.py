    "### Step 3: Stage 1 - Signal Generation\n",
    "We compute three independent signals to represent semantic urgency:\n",
    "1. **Signal 1: Rule-based NLP score** based on matched keywords and escalation phrases.\n",
    "2. **Signal 2: Embedding-based score** using sentence-transformers, KMeans clustering, and cosine similarity to extreme anchors.\n",
